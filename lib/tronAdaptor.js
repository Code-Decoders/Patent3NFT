const getContract = async () => {
  const instance = await tron.contract(abi, contractAddress);
  return instance;
};

const mint = async (ipfs, category) => {
  await contract.mint(ipfs, category).send({ from: address });
};
const makeOffer = async (tokenId, bid) => {
  await contract
    .makeOffer(tokenId.toString())
    .send({ from: address, callValue: parseInt((bid * 1000000).toString()) });
};
const cancelOffer = async (tokenId) => {
  await contract.cancelOffer(tokenId.toString()).send({ from: address });
};
const approveOffer = async (tokenId) => {
  await contract.approveOffer(tokenId.toString()).send({ from: address });
};

const getFromCategory = async (category) => {
  return await contract.getFromCategory(category).call();
};
const getFromTokenId = async (id) => {
  return await contract.getFromTokenId(id.toString()).call();
};

const parseAddress = (address) => {
  return tron.address.fromHex(address);
};

const parseUint = (uint) => {
  return tron.toDecimal(uint) / 1000000;
};

export {
  initializeTron,
  mint,
  getFromCategory,
  parseAddress,
  parseUint,
  getFromTokenId,
  makeOffer,
  cancelOffer,
  approveOffer,
};
